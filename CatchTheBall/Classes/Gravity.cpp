#include "Gravity.h"

/////////////////////////////////////////////////////
//引数			:なし
//戻り値		:なし
//動作			:値の初期化
/////////////////////////////////////////////////////
Gravity::Gravity()
{
	//初期化
	y = 0;
	gravity = 0;
}

/////////////////////////////////////////////////////
//引数			:なし
//戻り値		:なし
//動作			:落下処理
/////////////////////////////////////////////////////
void Gravity::ObjGravity()
{
		gravity += Time::GetDeltaTime() * 0.1f;
		y += gravity;
}

/////////////////////////////////////////////////////
//引数			:なし
//戻り値		:なし
//動作			:重力リセット
/////////////////////////////////////////////////////
void Gravity::GravityReset()
{
	gravity = 0;
	y = 0;
}