#include "Gravity.h"

/////////////////////////////////////////////////////
//引数			:なし
//戻り値		:なし
//動作			:値の初期化
/////////////////////////////////////////////////////
Gravity::Gravity()
{
	//初期化
	y = 0;
	y_prev = y;
	y_temp = 0;
	gravity = -1;
}

/////////////////////////////////////////////////////
//引数			:なし
//戻り値		:なし
//動作			:落下処理
/////////////////////////////////////////////////////
void Gravity::ObjGravity()
{
		gravity -= 1;
		y_temp =  gravity;
		y -= y_temp;
}

/////////////////////////////////////////////////////
//引数			:なし
//戻り値		:なし
//動作			:重力リセット
/////////////////////////////////////////////////////
void Gravity::GravityReset()
{
	gravity = -1;
}