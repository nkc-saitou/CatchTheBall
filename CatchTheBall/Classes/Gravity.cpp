#include "Gravity.h"

/////////////////////////////////////////////////////
//引数			:なし
//戻り値		:なし
//動作			:値の初期化
/////////////////////////////////////////////////////
Gravity::Gravity()
{
	//初期化
	y = 0;
	y_prev = y;
	y_temp = 0;
}

/////////////////////////////////////////////////////
//引数			:なし
//戻り値		:なし
//動作			:落下処理
/////////////////////////////////////////////////////
void Gravity::ObjGravity()
{
	//Verlet法
	//http://d.hatena.ne.jp/Gemma/20080517/1211010942
	y_temp = y;
	y += (y - y_prev) + 1;
	y_prev = y_temp;
}

/////////////////////////////////////////////////////
//引数			:なし
//戻り値		:なし
//動作			:重力リセット
/////////////////////////////////////////////////////
void Gravity::GravityReset()
{
	y_prev = y;
}